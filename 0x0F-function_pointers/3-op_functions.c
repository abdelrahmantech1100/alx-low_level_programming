#include "3-calc.h"

/**
 * op_add - Check code
 * @a: first arg
 * @b: second arg
 *
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Check code
 * @a: first arg
 * @b: second arg
 *
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Check code
 * @a: first arg
 * @b: second arg
 *
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Check code
 * @a: first arg
 * @b: second arg
 *
 * Return: the quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -  Check code
 * @a: first arg
 * @b: second arg
 *
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
